const DEFAULT_LANGUAGE = "ru";
const RTL_LANGUAGES = new Set(["he"]);

function getSavedLanguage() {
  const saved = localStorage.getItem("language") || localStorage.getItem("lang");
  return translations[saved] ? saved : DEFAULT_LANGUAGE;
}

function translateElement(element, lang) {
  const dictionary = translations[lang] || translations[DEFAULT_LANGUAGE];
  const key = element.dataset.i18n || element.dataset.lang;
  if (key && dictionary[key]) element.textContent = dictionary[key];

  const placeholderKey = element.dataset.i18nPlaceholder || element.dataset.langPlaceholder || element.dataset.placeholder;
  if (placeholderKey && dictionary[placeholderKey]) element.placeholder = dictionary[placeholderKey];

  const ariaKey = element.dataset.i18nAria;
  if (ariaKey && dictionary[ariaKey]) element.setAttribute("aria-label", dictionary[ariaKey]);
}

function changeLanguage(lang) {
  const nextLang = translations[lang] ? lang : DEFAULT_LANGUAGE;
  const isRtl = RTL_LANGUAGES.has(nextLang);

  localStorage.setItem("language", nextLang);
  localStorage.setItem("lang", nextLang);

  document.documentElement.lang = nextLang;
  document.documentElement.dir = isRtl ? "rtl" : "ltr";
  document.body.dir = isRtl ? "rtl" : "ltr";
  document.body.classList.toggle("rtl", isRtl);

  document.querySelectorAll("[data-i18n], [data-lang], [data-i18n-placeholder], [data-lang-placeholder], [data-placeholder], [data-i18n-aria]").forEach((element) => {
    translateElement(element, nextLang);
  });

  const pageTitleKey = document.body.dataset.pageTitle;
  if (pageTitleKey && translations[nextLang][pageTitleKey]) {
    document.title = translations[nextLang][pageTitleKey];
  }

  document.querySelectorAll("#langSwitcher, #languageSwitcher").forEach((selector) => {
    selector.value = nextLang;
    if (!selector.dataset.i18nBound) {
      selector.dataset.i18nBound = "true";
      selector.addEventListener("change", (event) => changeLanguage(event.target.value));
    }
  });
}

document.addEventListener("DOMContentLoaded", () => changeLanguage(getSavedLanguage()));
document.addEventListener("navbar:loaded", () => changeLanguage(getSavedLanguage()));

window.changeLanguage = changeLanguage;
